"""마케팅 추론 벤치 v4 — '함정' 문제 모음(순진하게 풀면 틀리지만 코드는 정확).

각 문제의 정답은 *파이썬으로 닫힌 형식 계산* 한다(madobi: 산술은 코드가 보장).
함정 4종: Simpson 역설 · 어트리뷰션 중복 · 타임존 경계 · 베이즈 사후확률.
'판단'이 필요한 함정(LLM 영역)은 넣지 않는다 — 정답이 코드로 안 나오면 벤치가 아니다.

각 항목에 trap(순진한 오답)과 why(왜 그게 틀리고 정답이 맞는지)를 달아 교육적 가치도 보존.
"""
import json
from pathlib import Path

OUT = Path(__file__).parent / "reasoning4"
OUT.mkdir(parents=True, exist_ok=True)
P, A, i = [], {}, 0


def nid():
    global i
    i += 1
    return f"Z{i:02d}"


def add(prompt, fmt, ans, trap=None, why=None):
    pid = nid()
    item = {"id": pid, "group": "marketing", "prompt": prompt, "format": fmt}
    if trap is not None:
        item["trap"] = trap          # 순진하게 풀었을 때의 오답
    if why is not None:
        item["why"] = why
    P.append(item)
    A[pid] = str(ans)


# ── 함정 1: Simpson 역설 — 그룹별 비율의 '단순평균'은 틀림, 가중평균(Σ/Σ)이 정답 ──
# A: 클릭 1000, 전환 20 (CVR 2.0%) / B: 클릭 9000, 전환 450 (CVR 5.0%)
add("캠페인 A는 클릭 1,000·전환 20, B는 클릭 9,000·전환 450이다. 전체 통합 CVR(%)? 소수 둘째.",
    "퍼센트 소수 둘째", f"{(20 + 450) / (1000 + 9000) * 100:.2f}",
    trap="단순평균 (2.0+5.0)/2 = 3.50%",
    why="가중평균 Σ전환/Σ클릭 = 470/10000 = 4.70%. 볼륨이 큰 B가 통합값을 끌어올린다.")

# Simpson 역설 본체: 두 세그 각각 신규가 높은데 통합은 역전될 수 있다 — 통합은 가중합.
add("신규: 노출 100,000·클릭 1,000(CTR 1.00%). 기존: 노출 10,000·클릭 300(CTR 3.00%). "
    "통합 CTR(%)? 소수 둘째.", "퍼센트 소수 둘째",
    f"{(1000 + 300) / (100000 + 10000) * 100:.2f}",
    trap="단순평균 (1.00+3.00)/2 = 2.00%",
    why="가중평균 1300/110000 = 1.18%. 노출이 압도적인 신규(낮은 CTR)가 통합을 지배한다.")

# ── 함정 2: 어트리뷰션 중복 — 채널별 '라스트클릭 전환' 합이 실제 고유전환을 초과 ──
# 라스트클릭 합 = 120+80+50 = 250, 실제 고유 전환 = 200 → 중복 50
add("라스트클릭 기준 채널별 전환이 메타 120·구글 80·네이버 50건으로 합 250건인데, "
    "실제 고유 전환은 200건이다. 중복 집계된 전환 수는? 정수.",
    "정수(건)", (120 + 80 + 50) - 200,
    trap="채널 합 250을 그대로 보고",
    why="채널별 라스트클릭을 더하면 한 전환이 여러 채널에 잡혀 250-200=50건 과대계상.")

# 멀티터치(선형) 분배: 한 전환이 3터치면 각 1/3 — 합은 고유전환과 같아야(중복 0)
add("고유 전환 1건이 메타→구글→메타 3터치를 거쳤다. 선형 어트리뷰션에서 메타에 배분되는 전환 기여? 소수 둘째.",
    "소수 둘째", f"{2/3:.2f}",
    trap="메타가 두 번이니 1.00으로 셈",
    why="선형은 터치 수로 균등분배: 3터치 중 메타 2회 = 2/3 = 0.67. 합(2/3+1/3)=1로 고유전환 보존.")

# ── 함정 3: 타임존 경계 — KST 자정 부근 전환이 UTC 날짜로는 전날에 잡힘 ──
# KST = UTC+9. KST 2026-01-02 03:00 → UTC 2026-01-01 18:00 (UTC 기준 1일)
add("전환이 KST 2026-01-02 03:00에 발생했다. UTC(=KST-9h) 기준 날짜는? YYYY-MM-DD.",
    "YYYY-MM-DD", "2026-01-01",
    trap="KST 날짜 그대로 2026-01-02",
    why="03:00 KST에서 9시간 빼면 전날 18:00 UTC → UTC 집계는 1/1로 잡힌다(리포트 날짜 불일치 원인).")

# 9시간 차로 'KST 오늘'의 일부가 'UTC 어제'로 — 자정~09시 KST 전환 수
add("어떤 날 KST 00:00~08:59 사이 전환 35건, 09:00~23:59 전환 165건이다. "
    "UTC 날짜 기준으로 '전날'에 집계되는 전환 수는? 정수.",
    "정수(건)", 35,
    trap="전체 200건이 같은 날",
    why="KST 09:00 미만(=UTC 전날 15:00~23:59)인 35건만 UTC 기준 전날로 넘어간다.")

# ── 함정 4: 베이즈 사후확률 — '클릭했으면 관심'이 아니다. 기저율을 곱해야 ──
# P(관심)=0.10, P(클릭|관심)=0.40, P(클릭|무관심)=0.02
# P(관심|클릭) = (.10*.40)/(.10*.40 + .90*.02) = .04 / (.04+.018) = .04/.058
add("관심 사용자 비율 10%, 관심 시 클릭률 40%, 무관심 시 클릭률 2%다. "
    "클릭한 사용자가 실제 관심일 확률 P(관심|클릭)(%)? 소수 첫째.",
    "퍼센트 소수 첫째",
    f"{(0.10 * 0.40) / (0.10 * 0.40 + 0.90 * 0.02) * 100:.1f}",
    trap="클릭률 40%를 그대로 '관심 확률'로",
    why="베이즈: 사전 10%가 낮아 무관심의 우연클릭(0.9*0.02)이 분모를 키운다 → 약 69.0%, 40%가 아님.")

# 전환율 기반 사후: 노출 대비 구매가 드물 때 '클릭=구매의도' 과신 방지
add("클릭 중 5%가 구매한다(P(구매|클릭)=0.05). 구매자의 80%가 장바구니를 거쳤고(P(장바구니|구매)=0.80), "
    "전체 클릭의 20%가 장바구니를 담는다(P(장바구니)=0.20). P(구매|장바구니)(%)? 소수 첫째.",
    "퍼센트 소수 첫째", f"{0.05 * 0.80 / 0.20 * 100:.1f}",
    trap="구매율 5%를 장바구니에도 그대로 적용",
    why="베이즈: P(구매|장바구니)=P(장바구니|구매)·P(구매)/P(장바구니)=0.80·0.05/0.20=20.0%. 장바구니가 구매의도를 4배 높인다.")

(OUT / "problems.json").write_text(json.dumps(P, ensure_ascii=False, indent=2), encoding="utf-8")
(OUT / "answers.json").write_text(json.dumps(A, ensure_ascii=False, indent=2), encoding="utf-8")
print(f"built {len(P)} problems (v4, traps: Simpson/attribution/timezone/Bayes)")
